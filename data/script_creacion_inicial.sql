-- TABLES

CREATE TABLE LAS_PELOTAS.aeronaves (
    ID  INT   IDENTITY(1,1)    PRIMARY KEY,
    MATRICULA        NVARCHAR(255)    UNIQUE NOT NULL,
    MODELO        NVARCHAR(255),
    KG_DISPONIBLES    NUMERIC(18,0)    NOT NULL,
    FABRICANTE_ID    INT            NOT NULL,
    TIPO_SERVICIO_ID    INT            NOT NULL,
    BAJA            NVARCHAR(255),
    FECHA_ALTA      DATETIME        NOT NULL,
    CANT_BUTACAS    INT            NOT NULL,
    FECHA_BAJA    DATETIME,
    CONSTRAINT CK001 CHECK (BAJA IN ('DEFINITIVA', 'POR PERIODO'))
)

CREATE TABLE LAS_PELOTAS.fabricantes (
    ID  INT  IDENTITY(1,1)    PRIMARY KEY,
    NOMBRE        NVARCHAR(255)    NOT NULL
)

CREATE TABLE LAS_PELOTAS.tipos_de_servicios (
    ID INT   IDENTITY(1,1)    PRIMARY KEY,
    NOMBRE        NVARCHAR(255)    NOT NULL
)

CREATE TABLE LAS_PELOTAS.butacas (
    ID  INT     IDENTITY(1,1)    PRIMARY KEY,
    NUMERO        NUMERIC(18,0)    NOT NULL,
    TIPO            NVARCHAR(255),
    PISO            NUMERIC(18,0),
    AERONAVE_ID    INT            NOT NULL,
    ESTADO        NVARCHAR(255),
    CONSTRAINT CK002 CHECK (TIPO IN ('VENTANILLA', 'PASILLO')),
    CONSTRAINT CK003 CHECK (ESTADO IN ('LIBRE', 'COMPRADO'))
)

CREATE TABLE LAS_PELOTAS.pasajes (
    ID    INT    IDENTITY(1,1)    PRIMARY KEY,
    FECHA_COMPRA    DATETIME        NOT NULL,
    PRECIO        NUMERIC(18,2),
    CODIGO        NUMERIC(18,0)    UNIQUE NOT NULL,
    BUTACA_ID        INT            NOT NULL,
    CLIENTE_ID        INT            NOT NULL,
    BOLETO_COMPRA_ID INT
)

CREATE TABLE LAS_PELOTAS.clientes (
    ID   INT     IDENTITY(1,1)    PRIMARY KEY,
    ROL_ID        INT            NOT NULL,
    NOMBRE        NVARCHAR(255)    NOT NULL,
    APELLIDO        NVARCHAR(255),
    DNI            NUMERIC(18,0)    UNIQUE NOT NULL,
    DIRECCION        NVARCHAR(255),
    TELEFONO        NUMERIC(18,0),
    MAIL            NVARCHAR(255),
    FECHA_NACIMIENTO DATETIME,
    MILLAS_ACUMULADAS INT
)

CREATE TABLE LAS_PELOTAS.boletos_de_compra (
    ID INT IDENTITY(1,1)    PRIMARY KEY,
    CODIGO_COMPRA    NUMERIC(18,0)    NOT NULL,
    FECHA_COMPRA    DATETIME,
    PRECIO_COMPRA    NUMERIC(18,2),
    TIPO_COMPRA    NVARCHAR(255),
    CLIENTE_ID        INT            NOT NULL,
    CONSTRAINT CK004 CHECK (TIPO_COMPRA IN ('EFECTIVO', 'TARJETA'))
)

CREATE TABLE LAS_PELOTAS.roles (
    ID    INT    IDENTITY(1,1)    PRIMARY KEY
)

CREATE TABLE LAS_PELOTAS.funcionalidades (
    ID INT IDENTITY(1,1)    PRIMARY KEY,
    DETALLES        NVARCHAR(255)
)

CREATE TABLE LAS_PELOTAS.funcionalidades_por_rol (
	ROL_ID    INT,
	FUNCIONALIDAD_ID INT, 
	PRIMARY KEY(ROL_ID,FUNCIONALIDAD_ID)
)

CREATE TABLE LAS_PELOTAS.administradores (
    ID INT IDENTITY(1,1)    PRIMARY KEY,
    ROL_ID        INT            NOT NULL ,
    USERNAME        NVARCHAR(255)    UNIQUE NOT NULL,
    PASSWORD        NVARCHAR(255)    
)

CREATE TABLE LAS_PELOTAS.productos (
    ID  INT  IDENTITY(1,1)    PRIMARY KEY,
    NOMBRE        NVARCHAR(255)    UNIQUE,
    MILAS_REQUERIDAS INT,
    STOCK        INT        
)

CREATE TABLE LAS_PELOTAS.periodos_de_inactividad (
    ID    INT     IDENTITY(1,1)    PRIMARY KEY,
    DESDE        DATETIME,
    HASTA        DATETIME
)

CREATE TABLE LAS_PELOTAS.aeronaves_por_periodos (
    AERONAVE_ID INT,
    PERIODO_ID  INT,
    PRIMARY KEY(AERONAVE_ID,PERIODO_ID)
)

CREATE TABLE LAS_PELOTAS.servicios_por_rutas (
	TIPO_SERVICIO_ID   INT  ,
    RUTA_ID   INT,
    PRIMARY KEY(TIPO_SERVICIO_ID, RUTA_ID)
)

CREATE TABLE LAS_PELOTAS.aeropuertos (
    ID  INT    IDENTITY(1,1)    PRIMARY KEY,
    NOMBRE        NVARCHAR(255),
    CIUDAD_ID        INT             NOT NULL
)

CREATE TABLE LAS_PELOTAS.vuelos (
    ID     INT    IDENTITY(1,1)     PRIMARY KEY,
    FECHA_SALIDA     DATETIME,
    FECHA_LLEGADA     DATETIME,
    FECHA_LLEGADA_ESTIMADA DATETIME,
    AERONAVE_ID     INT            NOT NULL,
    RUTA_ID         INT            NOT NULL
)

CREATE TABLE LAS_PELOTAS.rutas (
    ID     INT     IDENTITY(1,1)     PRIMARY KEY,
    CODIGO         NUMERIC(18,0)    UNIQUE NOT NULL,
    PRECIO_BASE_KG     NUMERIC(18,2),
    PRECIO_BASE_PASAJE NUMERIC(18,2),
    ORIGEN_ID        INT            NOT NULL,
    DESTINO_ID        INT            NOT NULL,
    TIPO_SERVICIO_ID    INT            NOT NULL
)

CREATE TABLE LAS_PELOTAS.encomiendas (
    ID  INT    IDENTITY(1,1)    PRIMARY KEY,
    BOLETO_COMPRA_ID INT,
    VUELO_ID         INT
)

CREATE TABLE LAS_PELOTAS.ciudades (
    ID     INT     IDENTITY(1,1)     PRIMARY KEY,
    NOMBRE         NVARCHAR(255)    NOT NULL
)

CREATE TABLE LAS_PELOTAS.paquetes(
    ID   INT  IDENTITY(1,1)     PRIMARY KEY,
    CODIGO         NUMERIC(18,0)    UNIQUE NOT NULL,
    PRECIO         NUMERIC(18,2),
    KG             NUMERIC(18,0),
    FECHA         DATETIME,
    ENCOMIENDA_ID     INT
)

CREATE TABLE LAS_PELOTAS.canjes (
    ID     INT    IDENTITY(1,1)     PRIMARY KEY,
    CLIENTE_ID         INT             NOT NULL,
    PRODUCTO_ID     INT             NOT NULL,
    CANTIDAD         INT,
    FECHA         DATETIME
)

CREATE TABLE LAS_PELOTAS.tarjetas_de_credito (
    ID   INT   IDENTITY(1,1)     PRIMARY KEY,
    TIPO             NVARCHAR(255),
    NUMERO         NUMERIC(18,0)    UNIQUE NOT NULL,
    CODIGO_SEGURIDAD NUMERIC(18,0)    NOT NULL,
    FECHA_VTO         DATETIME        NOT NULL,
    CLIENTE_ID         INT            NOT NULL
)

CREATE TABLE LAS_PELOTAS.cancelaciones (
    ID   INT   IDENTITY(1,1)     PRIMARY KEY,
    FECHA_DEVOLUCION DATETIME,
    BOLETO_COMPRA_ID INT            NOT NULL,
    MOTIVO         NVARCHAR(255)
)

